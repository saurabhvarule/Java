

class SwitchDemo2{


	public static void main(String [] args){

		char ch ='A';
		switch(ch){

			case 'A': 
				System.out.println("Character A");
				break;

			case 'B':
				System.out.println("Charater B ");
				break;

			case 'C':
				System.out.println("Character C");
				break;
			
			default:
				System.out.println("Default");
				break;


		}


		



	}
}
