

class VowCons{

	public static void main(String [] args){

		char ch ='A';
		switch(ch){
			case'A':
		 	case'a':
			case'E':
			case'e':
			case'I':	
			case'i':	
			case'O':	
			case'o':	
			case'U':	
			case'u':
				System.out.println(ch + " : is a vowel");
				break;
			default:
				System.out.println(ch + " : is a constant");
				break;



		}
		}
}
